def add_postprocessing_to_vae(vae_model):
    """
    Wrap a VAE model's generate method with post-processing.
    
    Usage:
        vae = load_vae_model('dream_machine_vae.pth')
        vae = add_postprocessing_to_vae(vae)
        
        # Now generates valid outputs automatically!
        society = vae.generate()
    """
    original_generate = vae_model.generate
    
    def generate_with_postprocessing(*args, **kwargs):
        raw_output = original_generate(*args, **kwargs)
        fixed_output = fix_vae_output(raw_output, column_names=vae_model.column_names)
        return fixed_output
    
    vae_model.generate = generate_with_postprocessing
    return vae_model



"""
VAE Output Post-Processing
Fixes invalid values generated by the VAE to ensure all outputs are valid.

Usage:
    from vae_postprocessor import fix_vae_output
    
    raw_output = vae.generate()  # Generates invalid values
    fixed_output = fix_vae_output(raw_output)  # Fixes them!
"""

import numpy as np
